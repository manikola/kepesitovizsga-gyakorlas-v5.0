package hu.nive.ujratervezes.kepesitovizsga.snowwhite;

public class Dwarf {

    private String name;
    private int age;

    public Dwarf(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }
}
